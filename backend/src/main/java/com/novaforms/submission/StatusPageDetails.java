package com.novaforms.submission;

public class StatusPageDetails {
    private String title;
    private String subtitle;
    private String description;
    private String illustration;
    private String animation;
    private String accent;
    private String primaryButton;
    private String redirectUrl;
    private String backgroundVariant;
    private String icon;

    public StatusPageDetails() {}

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSubtitle() {
        return subtitle;
    }

    public void setSubtitle(String subtitle) {
        this.subtitle = subtitle;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getIllustration() {
        return illustration;
    }

    public void setIllustration(String illustration) {
        this.illustration = illustration;
    }

    public String getAnimation() {
        return animation;
    }

    public void setAnimation(String animation) {
        this.animation = animation;
    }

    public String getAccent() {
        return accent;
    }

    public void setAccent(String accent) {
        this.accent = accent;
    }

    public String getPrimaryButton() {
        return primaryButton;
    }

    public void setPrimaryButton(String primaryButton) {
        this.primaryButton = primaryButton;
    }

    public String getRedirectUrl() {
        return redirectUrl;
    }

    public void setRedirectUrl(String redirectUrl) {
        this.redirectUrl = redirectUrl;
    }

    public String getBackgroundVariant() {
        return backgroundVariant;
    }

    public void setBackgroundVariant(String backgroundVariant) {
        this.backgroundVariant = backgroundVariant;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }
}
